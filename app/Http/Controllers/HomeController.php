    public function store(Request $request)
    {
        //
        Home::create([
            "name" => $request->input('name'),
            "email" => $request->input('email'),
            "city" => $request->input('city'),
            "password" => Crypt::encrypt($request->input('password'))
        ]);
        return redirect('home');
    }

